/** web_search 기본 상한 (기획 확정: 생성 1회당 검색 5회). */
export const RESEARCH_MAX_WEB_SEARCHES = 5

/**
 * v6.0(작업R③): 미등재·저입력 상품 확장 상한(7회).
 * fruit-facts 사전에 없거나 입력이 빈약해 품종 특성·용도를 검색으로 더 채워야 하는
 * 케이스에서만 검색 각도를 늘린다(비용 가드: 그 외에는 기본 5회 유지).
 */
export const RESEARCH_MAX_WEB_SEARCHES_EXTENDED = 7

/** research 응답 max_tokens — v4.2 출력 필드 증가(소구점·불만·명칭)로 1,500→2,000. */
export const RESEARCH_MAX_TOKENS = 2000

/**
 * v6.0(작업R③): 이번 조사에 허용할 web_search 상한을 입력 신호로 결정.
 * - 미등재(fruit-facts 미매칭) 또는 저입력(품종·당도 모두 없음): 확장 7회
 *   → 검색 각도를 품종 특성·용도(조리·활용)에 더 배분.
 * - 그 외: 기본 5회(비용 가드).
 * 순수 함수 — adapter의 max_uses와 buildResearchMessages의 프롬프트 문구가 같은 값을 쓰도록.
 */
export function researchSearchBudget(input: CopyInput): number {
  const unregistered = detectFruitFactKey(input.productType) == null
  const lowInput = !input.variety?.trim() && input.brix == null
  return unregistered || lowInput
    ? RESEARCH_MAX_WEB_SEARCHES_EXTENDED
    : RESEARCH_MAX_WEB_SEARCHES
}

